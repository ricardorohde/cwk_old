	<li>O agendamento estará disponível para exclusão até 3h de antecedência ao horário agendado.</li>
	<li>Caso queira realizar o cancelamento ou remarcação dentro dessas 3h que antecedem o horario do agendamento. Será cobrado uma taxa equivalente a meia hora (1/2h) desse serviço ou descontado dos créditos de pacotes de horas. </li>
	<li>No caso de não comparecimento ou não cancelamento do agendamento, será cobrado o valor da hora integral do serviço ou descontado dos créditos de pacotes de horas.</li>
