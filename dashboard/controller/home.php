</ul>

<strong>EXCLUIR AGENDAMENTO</strong>
<ul>
 	<li>1 PASSO</li>
 	<li>No menu lateral clicar em Histórico do serviço que deseja realizar a exclusão do agendamento.</li>
 	<li>2 PASSO</li>
 	<li>Na lista dos agendamentos identifique o agendamento que deseja excluir</li>
	<li>3 PASSO</li>
 	<li>Na coluna açoes clique no botão excluir referente a linha do agendamento desejado.</li>
</ul>

<strong>HISTÓRICO DE AGENDAMENTOS</strong>
<ul>
