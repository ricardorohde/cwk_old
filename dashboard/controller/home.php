<strong>CADASTRANDO NOVO AGENDAMENTO</strong>
<ul>
 	<li>1º Passo: No menu lateral clicar em Agenda do serviço que deseja realizar o agendamento.</li>
 	<li>2º Passo: Basta clicar, segurar e arrastar no dia e hora desejado no calendário que aparecer.</li>
 	<li>3º Passo: Preencher informações, colocando nome completo + empresa/Ramo de atividade e concluir agendamento.</li>
</ul>

<strong>EXCLUIR AGENDAMENTO</strong>
<ul>
 	<li>1º Passo: No menu lateral clicar em Histórico do serviço que deseja realizar a exclusão do agendamento.</li>
 	<li>2º Passo: Na lista dos agendamentos identifique o agendamento que deseja excluir</li>
 	<li>3º Passo: Na coluna ações clique no botão excluir referente a linha do agendamento desejado.</li>
</ul>

<strong>HISTÓRICO DE AGENDAMENTOS</strong>
